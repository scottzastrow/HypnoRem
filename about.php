<div class="content">
HypnoRem is an app designed to aid in achieving a healthy sleep pattern. If you have difficulty getting to sleep, difficulty staying asleep, if you experience intermittent wakefulness during the night, or a combination of any or all of these, then HypnoRem can help.

<p>What does it do? HypnoRem has five themes to choose from. Each theme has 14 different soundscapes it cycles through. All of these soundscapes are comprised of low-amplitude mixed-frequency sound wave patterns similar to that of brain waves during REM sleep.
</p>
<p>REM sleep occurs in cycles of about 90-120 minutes throughout the night. Being able to cycle through REM and non-REM sleep is essential to achieving and maintaining a healthy sleep pattern.
</p>
<p>When HypnoRem is set to ‘Sleep Mode’ for both Volume and Cycle settings, it gradually drops the volume and extends the time for each soundscape. Think of it as gradually taking the training wheels off while you are still riding the bike.
</p>
<p>Because the wave patterns of HypnoRem and REM sleep are very similar to wave patterns during the waking state; HypnoRem is also an excellent relaxation tool.
</p>
The five themes:<ul>
<li>Forest Night</li>
<li>Ocean Depths</li>
<li>Thunderstorm</li>
<li>Tropic Wave</li>
<li>Wind Blown</li>
</ul>
<p>
&#8658; <a href="support.php#theme-selector">More on HypnoRem themes</a>
</p>
Things to note: When HypnoRem is NOT set to sleep mode for the Volume setting, it will infinitely loop, so a power source is necessary for extended sessions.
